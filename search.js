// create variables for collection, album, song arrays, and results array
const collectionArr = catalog.collections;
const albumArr = [];
const songArr = [];
const resultsArr = [];

// add albums and songs to arrays
for (let i = 0; i < collectionArr.length; i++) {
  let albums = collectionArr[i].albums;
  for (let i = 0; i < albums.length; i++) {
    let tracklist = albums[i].tracklist;
    albumArr.push(albums[i]);
    for (let i = 0; i < tracklist.length; i++) {
      songArr.push(tracklist[i]);
    }
  }
}

// function to push all songs to results array
function allSongs() {
  resultsArr.length = 0;
  for (let i = 0; i < songArr.length; i++) {
    resultsArr.push(songArr[i]);
  }
}

// function to search by song title
function searchSongTitle(query) {
  resultsArr.length = 0;
  for (let i = 0; i < songArr.length; i++) {
    let songTitle = songArr[i].songTitle;
    if (songTitle.toUpperCase().indexOf(query.toUpperCase()) > -1) {
      resultsArr.push(songArr[i]);
    }
  }
}

// function to search by producer
function searchProducer(query) {
  resultsArr.length = 0;
  for (let i = 0; i < songArr.length; i++) {
    let song = songArr[i];
    let producers = song.producers.join(", ");
    if (producers.toUpperCase().indexOf(query.toUpperCase()) > -1) {
      resultsArr.push(song);
    }
  }
}

// function to search by writer
function searchWriter(query) {
  resultsArr.length = 0;
  for (let i = 0; i < songArr.length; i++) {
    let song = songArr[i];
    let writers = song.writers.join(", ");
    if (writers.toUpperCase().indexOf(query.toUpperCase()) > -1) {
      resultsArr.push(song);
    }
  }
}

// function to filter self written tracks
function filterSelfWritten() {
  for (let i = resultsArr.length - 1; i >= 0; i--) {
    let writers = resultsArr[i].writers;
    if (writers.toString() !== "Taylor Swift") {
      resultsArr.splice(i, 1);
    }
  }
}

// function to filter alt versions
function filterAltVersion() {
  for (let i = resultsArr.length - 1; i >= 0; i--) {
    if (resultsArr[i].alternateVersion === true) {
      resultsArr.splice(i, 1);
    }
  }
}

// function to filter songs taylor does not own
function filterTaylorOwns() {
  for (let i = resultsArr.length - 1; i >= 0; i--) {
    if (resultsArr[i].taylorOwns === false) {
      resultsArr.splice(i, 1);
    }
  }
}

// function to filter by album. parameters should be the albums you WANT to include
function filterByAlbum(albums) {
  for (let i = resultsArr.length - 1; i >= 0; i--) {
    let onAlbum = resultsArr[i].onAlbum;
    if (albums.includes(onAlbum) === false) {
      resultsArr.splice(i, 1);
    }
  }
}

// function to filter by collections. parameters should be the collections you WANT to include
function filterByCollection(collections) {
  for (let i = resultsArr.length - 1; i >= 0; i--) {
    let inCollection = resultsArr[i].inCollection;
    if (collections.includes(inCollection) === false) {
      resultsArr.splice(i, 1);
    }
  }
}

